/**
 * while và do while
 */

let number = 1;

while (number < 10) {
  console.log('number is' + number);
  //điều kiện dừng
  // number = number + 1;
  // number+=1;
  number++;
}

// do{

// }while(condition: điều kiện)
let number2 = 1;
do {
  console.log('number is' + number2);
  number2++;
} while (number2 < 10);
